using S = long long;
using F = long long;

const S INF = 8e18;

S op(S a, S b){ return std::min(a, b); }
S e(){ return INF; }
S mapping(F f, S x){ return f+x; }
F composition(F f, F g){ return f+g; }
F id(){ return 0; }

int main() {
    int N, Q;
    cin >> N >> Q;
    string inputS;
    cin >> inputS;
    std::vector<S> v(N+1);
    v[0] = 0;
    rep(i,N){
        v[i+1] = v[i] + (inputS[i] == '(' ? 1: -1);
    }
    dump(v);
    atcoder::lazy_segtree<S, op, e, F, mapping, composition, id> seg(v);

    while(Q--){
        int type;
        int l, r;
        cin >> type >> l >> r;
        l--, r--;
        dump(type, l, r);
        // swapする
        if(type == 1){
            char c = inputS[l];
            char d = inputS[r];
            if(c == d){
                continue;
            }
            // 異なる時
            swap(inputS[l], inputS[r]);
            if(d == ')'){
                seg.apply(l+1, N+1, -2);
            }
            else{
                seg.apply(l+1, N+1, +2);
            }
            if(c == '('){
                seg.apply(r+1, N+1, +2);
            }
            else{
                seg.apply(r+1, N+1, -2);
            }
        }
        else{
            int lv = seg.prod(l, l+1);
            int rv = seg.prod(r+1, r+2);
            int minn = seg.prod(l, r+2);
            dump(l, r);
            dump(lv, rv, minn);
            if(lv != rv){
                cout << "No" << endl;
                continue;
            }
            if(minn < lv){
                cout << "No" << endl;
                continue;
            }
            cout << "Yes" << endl;
        }
    }
}

